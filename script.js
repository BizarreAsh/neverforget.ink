let currentLang = localStorage.getItem('nf_lang') || 'ru';

const langBtns = document.querySelectorAll('.lang-btn');
const i18nEls  = document.querySelectorAll('[data-i18n], [data-i18n-placeholder]');

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('nf_lang', lang);
  document.documentElement.lang = lang;
  const t = translations[lang];
  i18nEls.forEach(el => {
    if (el.dataset.i18n)            el.textContent = t[el.dataset.i18n]            ?? el.textContent;
    if (el.dataset.i18nPlaceholder) el.placeholder = t[el.dataset.i18nPlaceholder] ?? el.placeholder;
  });
  document.querySelectorAll('[data-genre]').forEach(el => {
    el.textContent = t[el.dataset.genre] ?? el.textContent;
  });
  langBtns.forEach(btn => btn.classList.toggle('active', btn.dataset.lang === lang));
  // Show only the book section matching the selected language
  document.querySelectorAll('[data-lang-section]').forEach(s => {
    s.style.display = s.dataset.langSection === lang ? '' : 'none';
  });
}

langBtns.forEach(btn => btn.addEventListener('click', () => setLanguage(btn.dataset.lang)));
setLanguage(currentLang);

// Auto-detect language on first visit (no stored preference)
if (!localStorage.getItem('nf_lang')) {
  fetch('https://ipapi.co/json/')
    .then(r => r.json())
    .then(data => {
      const cc = (data.country_code || '').toUpperCase();
      let lang = 'en';
      if (['RU','UA','BY','KZ','UZ','TJ','KG','AM','AZ','GE','MD','TM'].includes(cc)) lang = 'ru';
      else if (['DE','AT','CH','LI','LU'].includes(cc)) lang = 'de';
      setLanguage(lang);
    })
    .catch(() => {});
}

// --- Favorites (localStorage) ---

const FAVORITES_KEY = 'nf_favorites';

function loadFavorites() {
  try { return JSON.parse(localStorage.getItem(FAVORITES_KEY)) || []; }
  catch { return []; }
}

function saveFavorites(favs) {
  localStorage.setItem(FAVORITES_KEY, JSON.stringify(favs));
}

function isFavorite(title) {
  return loadFavorites().some(f => f.title === title);
}

function toggleFavorite(title, author, book) {
  const favs = loadFavorites();
  const idx  = favs.findIndex(f => f.title === title);
  if (idx === -1) favs.push({ title, author, book: book || '' });
  else            favs.splice(idx, 1);
  saveFavorites(favs);
  return idx === -1;
}

// --- Read history (localStorage) ---

const READ_KEY = 'nf_read';

function loadRead() {
  try { return JSON.parse(localStorage.getItem(READ_KEY)) || []; }
  catch { return []; }
}

function isRead(title) {
  return loadRead().some(r => r.title === title);
}

function toggleRead(title, author, book) {
  const list = loadRead();
  const idx  = list.findIndex(r => r.title === title);
  if (idx === -1) list.push({ title, author, book: book || '' });
  else            list.splice(idx, 1);
  localStorage.setItem(READ_KEY, JSON.stringify(list));
  return idx === -1;
}

function makeReadBtn(title, author, book) {
  const btn = document.createElement('button');
  btn.className = 'read-btn';
  const sync = () => {
    const on = isRead(title);
    btn.classList.toggle('active', on);
    btn.textContent = on ? '✓' : '○';
    btn.title = on ? '' : '';
  };
  sync();
  btn.addEventListener('click', e => {
    e.stopPropagation();
    toggleRead(title, author, book);
    sync();
  });
  return btn;
}

// --- Book cards on index.html (static) ---

document.querySelectorAll('.book-card').forEach(card => {
  const displayTitle  = card.querySelector('h3').textContent;
  const displayAuthor = card.querySelector('p').textContent;
  const book          = card.dataset.book || '';

  const favBtn = card.querySelector('.fav-btn');
  if (favBtn) {
    const sync = () => {
      const on = isFavorite(displayTitle);
      favBtn.classList.toggle('active', on);
      favBtn.textContent = on ? '♥' : '♡';
    };
    sync();
    favBtn.addEventListener('click', e => {
      e.stopPropagation();
      toggleFavorite(displayTitle, displayAuthor, book);
      sync();
    });
  }

  card.appendChild(makeReadBtn(displayTitle, displayAuthor, book));
});

// Single delegated handler for opening books — works for static and dynamic cards
document.addEventListener('click', e => {
  if (e.target.closest('.fav-btn, .read-btn')) return;
  const card = e.target.closest('[data-book]');
  if (card) window.location.href = card.dataset.book;
});

